//Primitive types->byte,short,int,long,char,float,double
//Reference types->String,Array,class
//TypeCasting ->Process of converting one type to another type.
//Primitive->Reference
//Primitive Types->Implicit and Explicit
//Implicit->Lower to Higher data type Eg: byte->int
//Explicit ->Higher to Lower data type Eg: int->byte
//Reference Types->Implicit and Explicit
//Implicit ->child to parent
//Explicit ->parent->child
public class DataTypes {
	
   public static void main(String[] args) {
	byte a = -50;
	byte h=90;
	short b =150;
	int c= 10000000;
	long d=837644446;
	char e='A';
	float f = 1.634737f;
	double g=6731747.88;
	int i = a+h;
	System.out.println(i);
}
}
